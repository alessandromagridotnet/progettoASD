public class Link {
    private String nome;
    private String automaI, automaF;
    private String eventoOn;

    public Link() {
        
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getAutomaI() {
        return automaI;
    }

    public void setAutomaI(String automaI) {
        this.automaI = automaI;
    }

    public String getAutomaF() {
        return automaF;
    }

    public void setAutomaF(String automaF) {
        this.automaF = automaF;
    }

    public String getEventoOn() {
        return eventoOn;
    }

    public void setEventoOn(String eventoOn) {
        this.eventoOn = eventoOn;
    }
    
    
}
